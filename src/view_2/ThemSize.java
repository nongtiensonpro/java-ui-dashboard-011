    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(kiemTraTrong()){
            boolean ketQua = sizeController.themTenSize(taoDoituong());
            if(ketQua){
                MsgBox.alert(this, "Bạn đã thêm sản phẩm thành công");
            }else{
                MsgBox.alert(this, "Bạn đã thêm sản phẩm thất bại");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    public SizeModel taoDoituong() {
        SizeModel s = new SizeModel();
        s.setMaSize(txtMaSize.getText());
        s.setSoSize(Integer.valueOf(txtSoSize.getText()));
        s.setMoTa(txtMoTa.getText());

        return s;
    }
    /**
     * @param args the command line arguments
     */
    private boolean kiemTraTrong(){
        
        if (txtMaSize.getText().trim().equals("")) {
            MsgBox.alert(this, "Bạn chưa nhập mã size!");
            return false;
        }
        if (txtMoTa.getText().trim().equals("")) {
            MsgBox.alert(this, "Bạn hãy nhập mô tả!");
            return false;
        }
        if (txtSoSize.getText().trim().equals("")) {
            MsgBox.alert(this, "Bạn chưa nhập số size!");
            return false;
        }
        return true;
    }
