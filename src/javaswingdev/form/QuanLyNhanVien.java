    private TaiKhoanNhanVIenFull taoNhanVienMoi() {
        TaiKhoanNhanVIenFull Tkfull = new TaiKhoanNhanVIenFull();
        Tkfull.setHoTen(txtHoTen.getText());
        Tkfull.setSoDienThoai(txtSoDienThoai.getText());
        Tkfull.setVaiTro(rdoQuanLy.isSelected());
        java.util.Date utilDate1 = txtNgayTao.getCalendar().getTime(); // Lấy đối tượng java.util.Date
        java.sql.Date ngayTao = new java.sql.Date(utilDate1.getTime()); // Chuyển đổi sang java.sql.Date
        Tkfull.setNgayTao(ngayTao);
        java.util.Date utilDate2 = txtNgayTao.getCalendar().getTime(); // Lấy đối tượng java.util.Date
        java.sql.Date ngaySua = new java.sql.Date(utilDate2.getTime()); // Chuyển đổi sang java.sql.Date
        Tkfull.setNgaySua(ngaySua);
        Tkfull.setTrangThai(rdoHoatDong.isSelected());
        Tkfull.setGioiTinh(rdoNam.isSelected());
        java.util.Date utilDate3 = txtNgayTao.getCalendar().getTime(); // Lấy đối tượng java.util.Date
        java.sql.Date ngaySinh = new java.sql.Date(utilDate3.getTime()); // Chuyển đổi sang java.sql.Date
        Tkfull.setNgaySinh(ngaySinh);
        Tkfull.setDiaChi(txtDiaChi.getText());
        Tkfull.setEmail(txtEmail.getText());
        return Tkfull;
    }

