    private void hienThiLenTable() {

        DefaultTableModel dtm = (DefaultTableModel) tblNhanVien.getModel();
        dtm.setRowCount(0);
        for (TaiKhoanNhanVIenFull taiKhoanNhanVIenFull : listTaiKhoanNhanVien) {
            dtm.addRow(new Object[]{
                taiKhoanNhanVIenFull.getHoTen(),
                taiKhoanNhanVIenFull.getSoDienThoai(),
                taiKhoanNhanVIenFull.isVaiTro() ? "Quản lý" : "Nhân viên",
                taiKhoanNhanVIenFull.getNgayTao(),
                taiKhoanNhanVIenFull.getNgaySua(),
                taiKhoanNhanVIenFull.getNgaySinh(),
                taiKhoanNhanVIenFull.isTrangThai() ? "Đang làm" : "Nghỉ làm",
                taiKhoanNhanVIenFull.isGioiTinh() ? "Nam" : "Nữ",
                taiKhoanNhanVIenFull.getDiaChi(),
                taiKhoanNhanVIenFull.getEmail()
            });
        }

    }

    private boolean kiemtratrongcactruong() {
        if (txtHoTen.getText().trim().equals("")) {
            MsgBox.alert(this, "Bạn cần nhập tên Nhân viên");
            return false;
        }
        if (txtDiaChi.getText().trim().equals("")) {
            MsgBox.alert(this, "Bạn cần nhập địa chỉ Nhân viên");
            return false;
        }
        if (txtEmail.getText().trim().equals("")) {
            MsgBox.alert(this, "Bạn cần email tên Nhân viên");
            return false;
        }
        if (txtSoDienThoai.getText().trim().equals("")) {
            MsgBox.alert(this, "Bạn cần nhập số điện thoaị Nhân viên");
            return false;
        }
        try {
            Date ngaySinh = txtNgaySinh.getDate();
        } catch (Exception e) {
            MsgBox.alert(this, "Bạn cần chọn ngày sinh");
            return false;

        }
        try {
            Date ngaytao = txtNgayTao.getDate();
        } catch (Exception e) {
            MsgBox.alert(this, "Bạn cần chọn ngày taọ");
        }
        try {
            Date ngaysua = txtNgaySua.getDate();
        } catch (Exception e) {
            MsgBox.alert(this, "Bạn cần chọn ngày sửa");
        }
        return true;
    }

