            TaiKhoanNhanVIenFull tknvif = listTaiKhoanNhanVien.get(tblNhanVien.getSelectedRow());
            txtHoTen.setText(tknvif.getHoTen());
            txtSoDienThoai.setText(tknvif.getSoDienThoai());
